package com.reactSpring.trailer.model;

import java.util.ArrayList;
import java.util.Date;
import java.util.List;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

import com.fasterxml.jackson.annotation.JsonIgnore;

@Entity
@Table(name = "comment")
public class Comment {
	
	@Id
	@GeneratedValue
	private Long id;
	private String content;
	@ManyToOne
	private User user;
	@ManyToMany(mappedBy = "parentComment")
	private List<Comment> comments;
	@JsonIgnore
	@ManyToOne
	private Comment parentComment;
	@JsonIgnore
	@ManyToOne
	private Movie movie;
	private Date date;
	
	
	public Comment() {
	}
	
	public Comment getParentComment() {
		return parentComment;
	}
	public void setParentComment(Comment parentComment) {
		parentComment.addComment(this);
		this.parentComment = parentComment;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public List<Comment> getComments() {
		return comments;
	}
	public void setComments(List<Comment> comments) {
		this.comments = comments;
	}
	public Movie getMovie() {
		return movie;
	}
	public void setMovie(Movie movie) {
		movie.addComment(this);
		this.movie = movie;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	@Override
	public String toString() {
		return "Comment [id=" + id + ", content=" + content + ", user=" + user + ", comments=" + comments
				+ ", parentComment=" + parentComment + ", movie=" + movie + ", date=" + date + "]";
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Comment other = (Comment) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
	public void addComment(Comment comment) {
		if(this.comments == null) {
			this.comments = new ArrayList<Comment>();
		}
		this.comments.add(comment);
	}
	
	
}
